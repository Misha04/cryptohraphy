def mul02(byte):
    """Множення байта на 02 у GF(2^8) з використанням m(x) = x^8 + x^4 + x^3 + x + 1."""
    byte <<= 1  # Зсув вліво на 1 біт (множення на x)
    if byte & 0x100:  # Якщо 9-й біт встановлено, виконуємо зведення за модулем m(x)
        byte ^= 0x1B  # m(x) = x^8 + x^4 + x^3 + x + 1 -> 0x1B — це молодші 8 біт
    return byte & 0xFF  # Повертаємо лише молодші 8 біт

def mul03(byte):
    """Множення байта на 03 у GF(2^8) з використанням m(x) = x^8 + x^4 + x^3 + x + 1."""
    return mul02(byte) ^ byte  # Множимо на 02 і виконуємо XOR з початковим байтом

# Приклади тестування
print(f"D4 * 02 = {hex(mul02(0xD4))}")  # Очікувано: 0xB3
print(f"BF * 03 = {hex(mul03(0xBF))}")  # Очікувано: 0xDA
